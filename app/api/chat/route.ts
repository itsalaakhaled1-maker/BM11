import { NextRequest, NextResponse } from "next/server";
import { aiChat } from "@/lib/ai-service";

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { question, context, initiativeTitle } = body;

    if (!question) {
      return NextResponse.json(
        { error: "السؤال مطلوب" },
        { status: 400 }
      );
    }

    const systemPrompt = `أنت مساعد باحث أكاديمي متخصص وخبير في مراجعة الأوراق البحثية المقارنة المعيارية.

🔹 أسلوبك في المحادثة:
- تحدث بالعربية الفصحى السهلة والواضحة
- استخدم فقرات قصيرة (3-5 أسطر كحد أقصى)
- استخدم العناوين الفرعية لتنظيم الإجابة
- استخدم الرموز التعبيرية (✅ ⚠️ 📊 🔍) باعتدال
- استخدم الجداول عند المقارنة بين شيئين
- ابدأ بجملة ترحيبية مختصرة ثم انتقل للإجابة
- ختم إجاباتك بنقطة خلاصة واحدة

🔹 قواعد الاستجابة:
- لا تنسخ المحتوى بالحرف — أعد صياغته بأسلوبك
- إذا طلب المستخدم "تلخيص" — قدم ملخصاً من 5-7 نقاط رئيسية
- إذا طلب المستخدم "شرح" — استخدم أمثلة توضيحية
- إذا طلب المستخدم "مقارنة" — استخدم جدولاً
- إذا طلب المستخدم "توصيات" — رقمها (1, 2, 3)
- إذا كان السؤال خارج نطاق البحث — اعتذر بأدب واقترح سؤالاً بديلاً

🔹 تنسيق الإجابة (Markdown فقط — لا HTML نهائياً):
- استخدم ### للعناوين الرئيسية
- استخدم **نص عريض** للنص المهم
- استخدم - أو • للنقاط
- استخدم 1. 2. 3. للترقيم
- استخدم | لعمل جداول
- استخدم سطر جديد بين كل فقرة
- لا تستخدم <h3> أو <p> أو <ul> أو <li> أو <strong> أو <br> نهائياً

عنوان البحث الحالي: ${initiativeTitle || "غير محدد"}`;

    const userPrompt = `مرحباً! أنا أحتاج مساعدتك في فهم البحث التالي.

📋 معلومات البحث:
${context || "لم يتم تحميل سياق البحث بعد"}

❓ سؤالي: ${question}

يرجى الإجابة بأسلوب واضح ومنسق وجميل، كأنك تتحدث معي مباشرة.

⚠️ مهم جداً: رجاءً رجع الإجابة بتنسيق Markdown فقط. استخدم ### للعناوين، **نص عريض** للنص المهم، - للنقاط، | للجداول. لا تستخدم HTML نهائياً.`;

    const messages = [
      { role: "system", content: systemPrompt },
      { role: "user", content: userPrompt },
    ];

    const reply = await aiChat(messages);

    return NextResponse.json({ reply });
  } catch (error) {
    console.error("Chat API error:", error);
    return NextResponse.json(
      { 
        error: "فشلت المحادثة",
        reply: "عذراً، حدث خطأ في معالجة سؤالك. يرجى المحاولة مرة أخرى." 
      },
      { status: 500 }
    );
  }
}